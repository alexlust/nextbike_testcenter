namespace Nextbike\Api\Tests\Command;

use Framework\Command\AbstractCommand;

class GetClosestLocationCommand extends AbstractCommand
{

    private $lat;
    private $lng;

    /**
     * @return mixed
     */
    public function getLat()
    {
        return $this->lat;
    }

    /**
     * @param mixed $lat
     */
    public function setLat($lat)
    {
        $this->lat = $lat;
    }

    /**
     * @return mixed
     */
    public function getLng()
    {
        return $this->lng;
    }

    /**
     * @param mixed $lng
     */
    public function setLng($lng)
    {
        $this->lng = $lng;
    }

}